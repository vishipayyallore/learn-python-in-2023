# This is a string literal
print('Hello Python World !')
print("Hello Python World !")

# This is a integer or float literal
print(100)
print(100.0938498)
print(-20)
print(-20.3847)

# This is boolean True or False
print(True)
print(False)

# This is a [] list
print([])
print([1, 2, 3])
print(["John", "Mary", "Bob"])
